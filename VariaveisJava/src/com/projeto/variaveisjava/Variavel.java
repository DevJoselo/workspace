package com.projeto.variaveisjava;

public class Variavel {

		//Variáveis
		//	byte - Números - 1, 2, 3
		//	int - Números - 100, 2303, 7 
		// long - Números - 10023037, 100000000
		
		// float - Número decimales 1.1234, 2.3455
		// double - Número decimales 1.12345678, 2.34556788
	
		// boolean - Valores booleanos - true / false
		// String - Textos - "café", "nome"
	public static void main(String[] args) {
		
		byte camelCase;
		byte minhaVariavelByte = 2;
		int minhaVariavelInt = 35;
		long minhaVariavelLong = 100000000;
		float minhaVariavelFloat = 1.3f;
		double minhaVariavelDouble = 1.12345678;
		boolean minhaVariavelBoolean = true;
		String minhaVariavelString = "Jose Juarez ";
		
		System.out.println(minhaVariavelByte);
		System.out.println(minhaVariavelInt);
		System.out.println(minhaVariavelLong);
		System.out.println(minhaVariavelFloat);
		System.out.println(minhaVariavelDouble);
		System.out.println(minhaVariavelBoolean);
		System.out.println(minhaVariavelString + minhaVariavelInt);
		
	}

}
